using Microsoft.EntityFrameworkCore;
using Tutor_X_Tution_Management.Data;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.AddDbContext<apiDbContext>(option =>
option.UseMySql(builder.Configuration.GetConnectionString("DefaultConnectionString"),
ServerVersion.AutoDetect(builder.Configuration.GetConnectionString("DefaultConnectionString"))));


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
